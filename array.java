package sit_college;

public class array {

    public static void main(String[] args) {

        String n[] = {"Rohan","Banda"};

        for (int i = 0; i < n.length; i++) {
            for (int j = 0; j < n[i].length(); j++) {
                System.out.println(n[i].charAt(j));
            }
            System.out.println();
        }
    
    
    for(int i=0;i<n.length;i++)
    {
    	System.out.println(n[i]);
    }
}
}